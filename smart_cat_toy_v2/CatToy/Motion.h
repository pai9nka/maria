/*
  ============================================================================
   ФАЙЛ: Motion.h
   НАЗНАЧЕНИЕ: Управление сервоприводами и лазером
  ============================================================================

   Содержит:
   - Инициализацию серв (initServos)
   - Включение/выключение лазера (setLaser)
   - Плавное движение PAN (panNudgeSmooth)
   - Плавное движение TILT (servoTiltSlowWrite)
   - Основную задачу FreeRTOS для управления движением (taskMotion)

  ============================================================================
*/

#ifndef MOTION_H
#define MOTION_H

#include <Arduino.h>
#include <ESP32Servo.h>
#include "SharedState.h"

// Внешние объекты сервоприводов (определены в CatToy.ino)
extern Servo servoPan;
extern Servo servoTilt;
extern int lastTiltPos;  // Последняя позиция TILT для плавного движения

// ============================================================================
// ВСПОМОГАТЕЛЬНЫЕ ФУНКЦИИ ДЛЯ РАСЧЁТА ИМПУЛЬСОВ PAN
// ============================================================================

//int panLeftUs(const RuntimeConfig& cfg);
//int panRightUs(const RuntimeConfig& cfg);

// ============================================================================
// ИНИЦИАЛИЗАЦИЯ
// ============================================================================

/**
 * @brief Инициализация сервоприводов
 *
 * Настройки:
 * - Частота 50 Гц (стандарт для SG90)
 * - Диапазон импульсов 500..2400 мкс (типично для SG90)
 * - PAN устанавливается в стоп (1500 мкс)
 * - TILT устанавливается в домашний угол
 */
void initServos();

// ============================================================================
// УПРАВЛЕНИЕ ЛАЗЕРОМ
// ============================================================================

/**
 * @brief Включить или выключить лазер
 * @param on true = включить, false = выключить
 *
 * Обновляет состояние gState.laserOn под мьютексом.
 */
void setLaser(bool on);

// ============================================================================
// ПЛАВНОЕ ДВИЖЕНИЕ PAN (ГОРИЗОНТАЛЬ)
// ============================================================================

/**
 * @brief Плавно повернуть PAN на один шаг в заданном направлении
 * @param toRight true = вправо, false = влево
 * @param cfg текущая конфигурация (содержит panDeltaUs, panStepMs и др.)
 *
 * Алгоритм:
 * 1. Плавный разгон за 6 шагов
 * 2. Поддержание скорости
 * 3. Плавное торможение за 6 шагов
 * 4. Остановка и пауза для успокоения сервы
 *
 * Это значительно уменьшает шум и износ по сравнению с резким стартом/стопом.
 */
void panNudgeSmooth(bool toRight, const RuntimeConfig& cfg);

// ============================================================================
// ПЛАВНОЕ ДВИЖЕНИЕ TILT (ВЕРТИКАЛЬ)
// ============================================================================

/**
 * @brief Плавно переместить TILT в новое положение
 * @param newPos целевой угол (градусы)
 * @param speed скорость движения (1 = медленно, 3 = быстро)
 *
 * Алгоритм:
 * - Двигается с шагом, равным speed
 * - Задержка между шагами 15 мс
 * - Обновляет lastTiltPos для следующего вызова
 */
void servoTiltSlowWrite(int newPos, int speed);

// ============================================================================
// ЗАДАЧА FREERTOS ДЛЯ ДВИЖЕНИЯ
// ============================================================================

/**
 * @brief Задача FreeRTOS, управляющая сервоприводами и лазером
 *
 * Логика работы (цикл):
 * - Режим CALIB: лазер выкл, серво паркуются
 * - Режим POWER SAVE: лазер выкл, минимум движений
 * - Кота нет: медленный поиск с короткими "тычками"
 * - Кот рядом, но далеко: спокойный режим, лазер вкл, небольшие покачивания
 * - Активная игра: динамичное движение лазера по траектории
 *
 * Запускается на ядре 1, приоритет 2.
 */
void taskMotion(void* pv);
void enterSleepMode();

#endif  // MOTION_H