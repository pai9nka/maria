/*
  ============================================================================
   ФАЙЛ: SharedState.h
   НАЗНАЧЕНИЕ: Определение структур данных, которыми обмениваются задачи
  ============================================================================

   ВСЕ задачи FreeRTOS имеют доступ к этим данным через мьютекс gStateMutex.
   Это обеспечивает потокобезопасность.

  ============================================================================
*/

#ifndef SHARED_STATE_H
#define SHARED_STATE_H

#include <Arduino.h>
#include "Config.h"

// ============================================================================
// 1. РЕЖИМЫ РАБОТЫ УСТРОЙСТВА
// ============================================================================

/**
 * @enum DeviceMode
 * @brief Основные режимы работы игрушки
 */
enum DeviceMode : uint8_t {
    MODE_RUN = 0,    ///< Нормальный режим: игра с котом
    MODE_CALIB = 1   ///< Режим калибровки: серво паркуются, лазер выключен
};

// ============================================================================
// 2. КОНФИГУРАЦИЯ (НАСТРОЙКИ, СОХРАНЯЕМЫЕ В FLASH)
// ============================================================================

/**
 * @struct RuntimeConfig
 * @brief Конфигурация, которую пользователь может менять через Serial-команды
 *
 * Все поля сохраняются в Preferences и восстанавливаются после перезагрузки.
 * Доступ к полям — только через мьютекс.
 */
struct RuntimeConfig {
    // Управление PAN (горизонтальная серва 360°)
    int panDeltaUs   = PAN_DELTA_US_DEF;   ///< Дельта для движения (мкс)
    int panStepMs    = PAN_STEP_MS_DEF;    ///< Длительность шага (мс)
    int panSettleMs  = PAN_SETTLE_MS_DEF;  ///< Пауза после остановки (мс)

    // Границы конуса внимания
    int coneHalfDeg  = CONE_HALF_DEG_DEF;  ///< Половина угла обзора (градусы)

    // Управление TILT (вертикальная серва 180°)
    int tiltHomeDeg  = TILT_HOME_DEG_DEF;  ///< Домашний угол (градусы)
    int tiltMinDeg   = TILT_MIN_DEG;       ///< Минимальный угол (градусы)
    int tiltMaxDeg   = TILT_MAX_DEG;       ///< Максимальный угол (градусы)
    int tiltSpeed    = 2;                  ///< Скорость движения TILT (1=медл, 3=быстр)

    // Звук
    bool soundEnabled = true;              ///< Включить/выключить звук
    uint8_t soundLevel = 2;               ///< Громкость: 1=тихо, 2=нормально, 3=громко
};

// ============================================================================
// 3. ТЕКУЩЕЕ СОСТОЯНИЕ СИСТЕМЫ
// ============================================================================

/**
 * @struct SharedState
 * @brief Динамическое состояние, которое меняется в реальном времени
 *
 * Доступ к полям — только через мьютекс gStateMutex.
 */
struct SharedState {
    // Данные с датчика
    uint16_t distanceMM = 8190;   ///< Текущая дистанция (мм), 8190 = "нет сигнала"
    bool sensorOk = false;        ///< Датчик отвечает? (true=OK)

    // Обнаружение кота
    bool catDetected = false;      ///< Кот в зоне (ближе CAT_DETECT_MM)
    bool activePlay = false;       ///< Активная игра (ближе CAT_ACTIVE_MM)
    bool powerSave = false;        ///< Режим экономии энергии (долго нет кота)

    uint32_t sessionStartMs = 0;    // Время начала текущего сеанса (мс)
    bool sessionActive = false;     // Сеанс активен?

    // Состояние актуаторов
    bool laserOn = false;          ///< Лазер включён?
    int tiltDeg = TILT_HOME_DEG_DEF;   ///< Текущий угол TILT (градусы)
    //int panUs = PAN_STOP_US;       ///< Текущий сигнал PAN (мкс)
    int panUs = 90;                // теперь хранит угол PAN (0-180 градусов)
    int panRelDeg = 0;             ///< Относительное положение PAN (условные градусы)

    // Служебное
    uint32_t lastCatSeenMs = 0;    ///< Время последнего обнаружения кота (мс)
    DeviceMode mode = MODE_RUN;    ///< Текущий режим (RUN или CALIB)
    // для управления сном
    bool isAsleep = false;        // Игрушка спит?
    uint32_t sleepStartMs = 0;    // Когда заснули
};

// ----------------------------------------------------------------------------
// ГЛОБАЛЬНЫЕ ПЕРЕМЕННЫЕ (определены в CatToy.ino, объявлены здесь как extern)
// ----------------------------------------------------------------------------
extern RuntimeConfig gCfg;           ///< Текущая конфигурация
extern SharedState gState;           ///< Текущее состояние
extern SemaphoreHandle_t gStateMutex; ///< Мьютекс для доступа
extern volatile bool systemReady;    ///< Флаг готовности системы

#endif  // SHARED_STATE_H