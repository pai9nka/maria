/*
  ============================================================================
   ФАЙЛ: Sensor.h
   НАЗНАЧЕНИЕ: Управление датчиком расстояния VL53L0X
  ============================================================================

   VL53L0X — лазерный датчик временипролётного (ToF) измерения расстояния.
   Работает по I2C, измеряет расстояние от 30 мм до 2000 мм (номинально).

  ============================================================================
*/

#ifndef SENSOR_H
#define SENSOR_H

#include <Arduino.h>
#include <Adafruit_VL53L0X.h>

// Внешний объект датчика (определён в CatToy.ino)
extern Adafruit_VL53L0X lox;

// ============================================================================
// ИНИЦИАЛИЗАЦИЯ
// ============================================================================

/**
 * @brief Инициализация датчика VL53L0X
 *
 * Проверяет наличие датчика на I2C шине (адрес по умолчанию 0x29).
 * В случае ошибки — устанавливает gState.sensorOk = false.
 *
 * @return true если датчик найден и инициализирован
 */
bool initSensor();

// ============================================================================
// ЗАДАЧА FREERTOS ДЛЯ ДАТЧИКА
// ============================================================================

/**
 * @brief Задача FreeRTOS для опроса датчика расстояния
 *
 * Логика:
 * - Выполняет измерение каждые 70 мс (~14 Гц)
 * - Обновляет gState.distanceMM, gState.sensorOk
 * - Если расстояние <= CAT_DETECT_MM — кот обнаружен
 * - Если расстояние <= CAT_ACTIVE_MM — активная игра
 * - Если кота нет > INACTIVE_TIMEOUT_MS — режим энергосбережения
 *
 * Запускается на ядре 1, приоритет 2.
 */
void taskSensor(void* pv);

#endif  // SENSOR_H