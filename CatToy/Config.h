// OLED дисплей SSD1306 (SPI интерфейс)
#define OLED_MOSI   23   // Master Out Slave In (данные)
#define OLED_CLK    18   // Clock (тактирование)
#define OLED_DC     2    // Data/Command (разделение команд и данных)
#define OLED_CS     5    // Chip Select (выбор чипа)
#define OLED_RESET  4    // Reset (сброс)

// Серво приводы
#define SERVO_PAN_PIN   19   // SG90 360° (непрерывное вращение) — горизонталь
#define SERVO_TILT_PIN  27   // SG90 180° (позиционное) — вертикаль

// Лазер и звук
#define LASER_CTRL_PIN  26   // Управление лазером (через транзистор)
#define SPEAKER_PIN     25   // Динамик/пищалка (через резистор 1 кОм)

// I2C для датчика VL53L0X
#define I2C_SDA_PIN     21
#define I2C_SCL_PIN     22

// ============================================================================
// 2. ПАРАМЕТРЫ ДАТЧИКА РАССТОЯНИЯ (мм)
// ============================================================================

/**
 * Если расстояние до объекта МЕНЬШЕ этого значения — считаем, что кот "в зоне"
 * 1200 мм = 1.2 метра — хорошая дистанция для обнаружения.
 */
static const uint16_t CAT_DETECT_MM = 1200;

/**
 * Если расстояние МЕНЬШЕ этого значения — кот активно играет (включаем динамику)
 * 900 мм = 90 см — кот уже близко, начинаем "дразнить" лазером.
 */
static const uint16_t CAT_ACTIVE_MM = 900;

/**
 * Таймаут бездействия (миллисекунды)
 * Если кота не видно дольше этого времени — переходим в режим экономии энергии.
 * 60 секунд = разумный компромисс между отзывчивостью и энергосбережением.
 */
static const uint32_t INACTIVE_TIMEOUT_MS = 60000;

// ============================================================================
// 3. ПАРАМЕТРЫ СЕРВОПРИВОДОВ
// ============================================================================
static const int PAN_MIN_DEG = 0;      // левый край
static const int PAN_MAX_DEG = 180;    // правый край
static const int PAN_HOME_DEG = 90;    // центр
// ----- PAN (SG90 360°, непрерывное вращение) -----
/**
 * Сигнал "стоп" для сервы непрерывного вращения.
 * 1500 микросекунд — стандартный нейтральный импульс для SG90 360°.
 * При этом значении мотор должен останавливаться.
 * Из-за разброса параметров может потребоваться небольшая подстройка.
 */
static const int PAN_STOP_US = 1500;

/**
 * Дельта от стопа для движения в одну сторону (микросекунды)
 * 120 мкс — хороший стартовый компромисс (не слишком быстро, не слишком медленно)
 * Диапазон допустимых значений: 60 .. 220
 */
static const int PAN_DELTA_US_DEF = 120;

/**
 * Длительность одного "шага" движения (миллисекунды)
 * Влияет на скорость вращения. 28 мс — умеренно.
 * Диапазон: 5 .. 120
 */
static const int PAN_STEP_MS_DEF = 28;

/**
 * Пауза после остановки сервы (миллисекунды)
 * Нужна, чтобы серво успела полностью остановиться перед следующим движением.
 * 70 мс — достаточно для SG90.
 */
static const int PAN_SETTLE_MS_DEF = 70;

/**
 * Величина одного "тычка" панорамы (условные градусы)
 * Используется для отслеживания относительного положения.
 * 4 градуса — небольшой шаг для плавного движения.
 */
static const int PAN_NUDGE_DEG = 4;

// ----- TILT (SG90 180°, позиционное) -----
/**
 * Минимальный физический угол для TILT (градусы)
 * Ниже этого значения серво не опускается (чтобы не удариться об основание).
 */
static const int TILT_MIN_DEG = 20;

/**
 * Максимальный физический угол для TILT (градусы)
 * Выше этого значения серво не поднимается.
 */
static const int TILT_MAX_DEG = 60;

/**
 * Домашний угол TILT (градусы) — центральное положение "конуса внимания"
 * 25° — лазер смотрит немного вверх от горизонтали.
 */
static const int TILT_HOME_DEG_DEF = 25;

/**
 * Половина угла "конуса внимания" (градусы)
 * Лазер движется в пределах [tilt_home - cone, tilt_home + cone]
 * 22° — охват ~44 градусов, этого достаточно.
 */
static const int CONE_HALF_DEG_DEF = 22;

// ============================================================================
// 4. ПАРАМЕТРЫ ЗВУКА
// ============================================================================

/**
 * Базовая частота для PWM (Гц)
 * 2000 Гц — хорошо слышно, но не раздражает.
 */
const int BUZZ_BASE_FREQ = 2000;

/**
 * Разрешение ШИМ (бит)
 * 8 бит = 256 уровней громкости
 */
const int BUZZ_RES_BITS = 8;

/**
 * Канал LEDC для ESP32 (устаревший параметр, оставлен для совместимости)
 * В новых версиях ядра канал назначается автоматически.
 */
const int BUZZ_CH = 0;

// ============================================================================
// 5. ПАРАМЕТРЫ ОПТИМИЗАЦИИ (менять с осторожностью)
// ============================================================================

/**
 * Частота опроса датчика (период в мс)
 * 70 мс ~ 14 Гц — достаточно для отслеживания кота.
 */
static const uint32_t SENSOR_PERIOD_MS = 70;

/**
 * Период обновления дисплея (мс)
 * 250 мс = 4 кадра в секунду — плавно и не нагружает процессор.
 */
static const uint32_t UI_PERIOD_MS = 250;

/**
 * Задержка в режиме калибровки (мс)
 */
static const uint32_t CALIB_DELAY_MS = 120;


// ========== УПРАВЛЕНИЕ СНОМ ==========
static const uint32_t IDLE_TIMEOUT_SEC = 90;       // Через это (сек) → переход в сон
static const uint8_t WAKEUP_HOURS[] = {8, 16, 22}; // Часы пробуждения (0-23)
static const uint8_t WAKEUP_COUNT = 3;             // Сколько раз в сутки
// ========== УПРАВЛЕНИЕ СЕАНСАМИ И СНОМ ==========
static const uint32_t SESSION_DURATION_SEC = 120;   // Длительность игры (сек)
static const uint32_t SLEEP_DURATION_SEC = 61;      //`43200;   // Сон 12 часов

// ========== УПРАВЛЕНИЕ СЕАНСАМИ И ГЛУБОКИМ СНОМ ==========
static const uint32_t DEEP_SLEEP_SEC = 61;          //  43200;       // Сон 12 часов (43200 сек)


#endif  // CONFIG_H