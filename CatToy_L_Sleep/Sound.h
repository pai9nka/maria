/*
  ============================================================================
   ФАЙЛ: Sound.h
   НАЗНАЧЕНИЕ: Заголовочный файл для модуля звуковых эффектов
  ============================================================================

   Содержит объявления функций для воспроизведения различных звуков:
   - Простые писки (beep)
   - Плавное изменение частоты (beepSweep)
   - Звуки птицы, мыши, трели
   - Рандомизированные звуки для активной и спокойной игры

  ============================================================================
*/

#ifndef SOUND_H
#define SOUND_H

#include <Arduino.h>
#include "SharedState.h"

// ============================================================================
// БАЗОВЫЕ ЗВУКОВЫЕ ФУНКЦИИ
// ============================================================================

/**
 * @brief Инициализация звуковой подсистемы (LEDC для ESP32)
 *
 * Настраивает ШИМ на пине SPEAKER_PIN для генерации звуковых частот.
 * Вызывается один раз в setup().
 */
void initSound();

/**
 * @brief Воспроизвести простой писк фиксированной частоты
 * @param freq Частота в герцах (20..20000 Гц, реально слышимо 200..8000)
 * @param durMs Длительность в миллисекундах
 *
 * Пример: beep(1500, 100) → писк 1500 Гц длительностью 0.1 секунды
 */
void beep(uint16_t freq, uint16_t durMs);

/**
 * @brief Воспроизвести "скользящий" тон (частота плавно меняется)
 * @param startFreq Начальная частота (Гц)
 * @param endFreq Конечная частота (Гц)
 * @param durMs Общая длительность (мс)
 * @param steps Количество шагов изменения (чем больше, тем плавнее)
 *
 * Пример: beepSweep(800, 2000, 300, 30) → сирена от 800 до 2000 Гц за 0.3 сек
 */
void beepSweep(int startFreq, int endFreq, int durMs, int steps = 20);

// ============================================================================
// ТЕМАТИЧЕСКИЕ ЗВУКИ (для кошачьей аудитории)
// ============================================================================

/**
 * @brief Оригинальный звук "птичка" (два коротких писка с повышением)
 *
 * Звук из первой версии: 2800 Гц → пауза → 3400 Гц.
 * Оставлен для совместимости.
 */
void soundBirdOriginal();

/**
 * @brief Варианты птичьих звуков (рандомизированные)
 * @param v Номер варианта (0, 1, 2) — выбирается случайно
 *
 * - Вариант 0: восходящая трель (1800→3200→2400 Гц)
 * - Вариант 1: короткий щебет (2600, 3100, 2800 Гц)
 * - Вариант 2: "кукушка" (1500→1300 Гц с паузой)
 */
void soundBirdVariant(int v);

/**
 * @brief Звук "трель" — серия коротких случайных писков
 *
 * Создаёт 3-8 импульсов со случайными частотами (1200-2800 Гц)
 * и паузами (20-60 мс). Имитирует стрекот насекомых.
 */
void soundTrill();

/**
 * @brief Звук "мышка" — короткие низкие щелчки
 *
 * 1200 Гц → 20 мс пауза → 1000 Гц.
 * Тихо, ненавязчиво.
 */
void soundMouse();

/**
 * @brief Звук для активной игры (рандомизированный)
 *
 * 40% шанс — птичка (три варианта)
 * 30% шанс — трель
 * 30% шанс — случайный писк 1000-3000 Гц
 *
 * Всегда разный, чтобы кот не привыкал.
 */
void soundActivePlay();

/**
 * @brief Звук для спокойной игры (кот рядом, но не активен)
 *
 * 60% шанс — мышиный писк
 * 40% шанс — случайный короткий писк 800-1500 Гц
 */
void soundCalm();

// ============================================================================
// ЗАДАЧА FREERTOS ДЛЯ ЗВУКА
// ============================================================================

/**
 * @brief Задача FreeRTOS, управляющая воспроизведением звуков
 *
 * Логика:
 * - В режиме CALIB или POWER SAVE — молчит
 * - В активной игре — soundActivePlay() со случайной паузой 0.5-2 сек
 * - В спокойной игре — soundCalm() с паузой 1.5-4.5 сек
 * - Без кота — редкие "вздохи" (20% шанс) с паузой 3-8 сек
 *
 * Запускается на ядре 0, приоритет 1.
 */
void taskSound(void* pv);

#endif  // SOUND_H