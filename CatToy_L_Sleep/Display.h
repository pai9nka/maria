/*
  ============================================================================
   ФАЙЛ: Display.h
   НАЗНАЧЕНИЕ: Управление OLED дисплеем SSD1306 (SPI интерфейс)
  ============================================================================

   Отображает:
   - Статус датчика и расстояние до кота
   - Режим работы (активная игра, спокойная, поиск, энергосбережение)
   - Состояние лазера, углы TILT и относительное положение PAN

  ============================================================================
*/

#ifndef DISPLAY_H
#define DISPLAY_H

#include <Arduino.h>
#include <Adafruit_GFX.h>
#include <Adafruit_SSD1306.h>

// Внешний объект дисплея (определён в CatToy.ino)
extern Adafruit_SSD1306 display;

// ============================================================================
// ИНИЦИАЛИЗАЦИЯ
// ============================================================================

/**
 * @brief Инициализация OLED дисплея SSD1306
 *
 * Настройки:
 * - Размер: 128x64 пикселей
 * - Интерфейс: SPI (пины из Config.h)
 * - Адрес: автоматический (для SPI не нужен)
 *
 * В случае ошибки выводит сообщение в Serial.
 */
void initDisplay();

// ============================================================================
// ЗАДАЧА FREERTOS ДЛЯ ОБНОВЛЕНИЯ ДИСПЛЕЯ
// ============================================================================

/**
 * @brief Задача FreeRTOS для обновления информации на OLED
 *
 * Обновляет экран каждые 250 мс (4 кадра в секунду).
 * Отображает:
 * - Строка 0: название проекта
 * - Строка 12: статус датчика (OK/ERR)
 * - Строка 22: расстояние в мм
 * - Строка 32: наличие кота (YES/NO)
 * - Строка 42: текущий режим
 * - Строка 54: состояние лазера, угол TILT, положение PAN
 *
 * Запускается на ядре 0, приоритет 1.
 */
void taskUI(void* pv);

#endif  // DISPLAY_H