#ifndef PREFERENCES_STORAGE_H
#define PREFERENCES_STORAGE_H

#include <Preferences.h>
#include "SharedState.h"

// Внешний объект Preferences
extern Preferences gPrefs;

// ============================================================================
// ОСНОВНЫЕ ФУНКЦИИ
// ============================================================================

/**
 * @brief Инициализация хранилища и загрузка настроек
 *
 * Загружает из flash значения параметров в gCfg.
 * Если параметр отсутствует (первый запуск) — оставляет значения по умолчанию.
 */
void initPreferences();

/**
 * @brief Сохранение текущих настроек в flash
 *
 * Сохраняет в flash все поля gCfg.
 * Вызывается по команде "save".
 */
void saveConfig();

#endif  // PREFERENCES_STORAGE_H